package com.bicitools.mjson;

/**
 *
 * @author jhony
 */
public class DatosPartesOrdenJson {
    
    private String idParte;
    private String tipoParte;
    private String nombre;
    private String valor;
    private String descripcion;

    /**
     * @return the idParte
     */
    public String getIdParte() {
        return idParte;
    }

    /**
     * @param idParte the idParte to set
     */
    public void setIdParte(String idParte) {
        this.idParte = idParte;
    }

    /**
     * @return the valor
     */
    public String getValor() {
        return valor;
    }

    /**
     * @param valor the valor to set
     */
    public void setValor(String valor) {
        this.valor = valor;
    }

    /**
     * @return the descripcion
     */
    public String getDescripcion() {
        return descripcion;
    }

    /**
     * @param descripcion the descripcion to set
     */
    public void setDescripcion(String descripcion) {
        this.descripcion = descripcion;
    }

    /**
     * @return the nombre
     */
    public String getNombre() {
        return nombre;
    }

    /**
     * @param nombre the nombre to set
     */
    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    /**
     * @return the tipoParte
     */
    public String getTipoParte() {
        return tipoParte;
    }

    /**
     * @param tipoParte the tipoParte to set
     */
    public void setTipoParte(String tipoParte) {
        this.tipoParte = tipoParte;
    }
    
    
    
    
}
