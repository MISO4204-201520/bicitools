package com.bicitools.mjson;

/**
 *
 * @author jhony
 */
public class RutasUsuarioJson {
    
    private String usuario;
    private String nombre;
    private String latIni;
    private String lngIni;
    private String latFin;
    private String lngFin;
    private String tiempo;
    private String distancia;

    /**
     * @return the usuario
     */
    public String getUsuario() {
        return usuario;
    }

    /**
     * @param usuario the usuario to set
     */
    public void setUsuario(String usuario) {
        this.usuario = usuario;
    }

    /**
     * @return the nombre
     */
    public String getNombre() {
        return nombre;
    }

    /**
     * @param nombre the nombre to set
     */
    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    /**
     * @return the latIni
     */
    public String getLatIni() {
        return latIni;
    }

    /**
     * @param latIni the latIni to set
     */
    public void setLatIni(String latIni) {
        this.latIni = latIni;
    }

    /**
     * @return the lngIni
     */
    public String getLngIni() {
        return lngIni;
    }

    /**
     * @param lngIni the lngIni to set
     */
    public void setLngIni(String lngIni) {
        this.lngIni = lngIni;
    }

    /**
     * @return the latFin
     */
    public String getLatFin() {
        return latFin;
    }

    /**
     * @param latFin the latFin to set
     */
    public void setLatFin(String latFin) {
        this.latFin = latFin;
    }

    /**
     * @return the lngFin
     */
    public String getLngFin() {
        return lngFin;
    }

    /**
     * @param lngFin the lngFin to set
     */
    public void setLngFin(String lngFin) {
        this.lngFin = lngFin;
    }

    /**
     * @return the tiempo
     */
    public String getTiempo() {
        return tiempo;
    }

    /**
     * @param tiempo the tiempo to set
     */
    public void setTiempo(String tiempo) {
        this.tiempo = tiempo;
    }

    /**
     * @return the distancia
     */
    public String getDistancia() {
        return distancia;
    }

    /**
     * @param distancia the distancia to set
     */
    public void setDistancia(String distancia) {
        this.distancia = distancia;
    }
    
    

}
