package com.bicitools.mjson;

/**
 *
 * @author Ludwing
 */
public class AlquilarProductoJson {
    
    private String empresa;
    private String articulo;
    private String usuario;
    
  
    public AlquilarProductoJson() {
    }

    
     @Override
    public String toString() {
        return new StringBuffer(" empresa : ").append(this.getEmpresa())
                .append(" articulo : ").append(this.getArticulo())
                .append(" usuario : ").append(this.getUsuario()).toString();
    }

    /**
     * @return the empresa
     */
    public String getEmpresa() {
        return empresa;
    }

    /**
     * @param empresa the empresa to set
     */
    public void setEmpresa(String empresa) {
        this.empresa = empresa;
    }

    /**
     * @return the articulo
     */
    public String getArticulo() {
        return articulo;
    }

    /**
     * @param articulo the articulo to set
     */
    public void setArticulo(String articulo) {
        this.articulo = articulo;
    }

    /**
     * @return the usuario
     */
    public String getUsuario() {
        return usuario;
    }

    /**
     * @param usuario the usuario to set
     */
    public void setUsuario(String usuario) {
        this.usuario = usuario;
    }


    
}
